use std::collections::HashMap;

pub fn lookup_morse(text: &str) -> &str {
    let morse: HashMap<&str, &str> = HashMap::from([
        ("A", "._"),
        ("B", "_..."),
        ("C", "_._."),
        ("D", "_.."),
        ("E", "."),
        ("F", ".._."),
        ("G", "__."),
        ("H", "...."),
        ("I", ".."),
        ("J", ".___"),
        ("K", "_._"),
        ("L", "._.."),
        ("M", "__"),
        ("N", "_."),
        ("O", "___"),
        ("P", ".__."),
        ("Q", "__._"),
        ("R", "._."),
        ("S", "..."),
        ("T", "_"),
        ("U", ".._"),
        ("V", "..._"),
        ("W", ".__"),
        ("X", "_.._"),
        ("Y", "_.__"),
        ("Z", "__.."),
        ("0", "_____"),
        ("1", ".____"),
        ("2", "..___"),
        ("3", "...__"),
        ("4", "...._"),
        ("5", "....."),
        ("6", "_...."),
        ("7", "__..."),
        ("8", "___.."),
        ("9", "____."),
        (".", "._._._"),
        (",", "__..__"),
        (" ", " "),
    ]);

    if morse.contains_key(&text) {
        morse.get(&text).unwrap()
    } else {
        "?"
    }
}
